def find_unique_chars(s):
    unique = True

    for i in range(len(s)):
        if i == 0:
            if s[i] in s[1:]:
                unique = False
        elif i == len(s):
            if s[i] in s[:i]:
                unique = False
        else:
            if (s[i] in s[:i]) or (s[i] in s[(i + 1):]):
                unique = False
    return unique


